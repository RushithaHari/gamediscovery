# ==============================================================================
# IMPORTS
# ==============================================================================
use strict;
use warnings;
require "../_shared/functions.pl";

# ==============================================================================
# MAIN LOOP
# ==============================================================================
while(<>){
    # split parts
    (my $source, my $type, my $target) = split(" ", $_, 3);

    # ==========================================================================
    # TARGET
    # ==========================================================================
    (my $capture) = $target =~ /.*Category:(.+)>/;
    if (!$capture){
        next;
    }
    my $targetId = id($capture);

    # ==========================================================================
    # SOURCE
    # ==========================================================================
    ($capture) = $source =~ /.*Category:(.+)>/;
    my $sourceId = id($capture);

    # ==========================================================================
    # OUTPUT
    # ==========================================================================
    print "$sourceId\tcategory\t$targetId\n";
}
